CEspProc::CEspProc(char *name, MaEspCb fn) : MaEspProc(name)
{
	espCallback = fn;
}

////////////////////////////////////////////////////////////////////////////////

CEspProc::~CEspProc()
{
}

////////////////////////////////////////////////////////////////////////////////

int CEspProc::run(MaRequest *rq, int argc, char **argv)
{
	return (*espCallback)(rq, argc, argv);
}

