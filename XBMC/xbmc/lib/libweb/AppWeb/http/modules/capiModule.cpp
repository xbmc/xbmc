class CEspProc : public MaEspProc {
  private:
	MaEspCb		espCallback;
  public:
				CEspProc(char *name, MaEspCb fn);
				~CEspProc();
	int			run(MaRequest *rq, int argc, char **argv);
};

////////////////////////////////////////////////////////////////////////////////
