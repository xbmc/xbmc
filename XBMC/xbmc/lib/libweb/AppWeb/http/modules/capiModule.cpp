void maSetHostDefaultPage(MaHost *host, char *path, char *fileName)
{
	MaDir	*dir;
	char	pathBuf[MPR_MAX_FNAME];

	mprAssert(host);
	mprSprintf(pathBuf, sizeof(pathBuf), "%s/%s", host->getDocumentRoot(), 
		path);
	dir = host->findBestDir(pathBuf);
	mprAssert(dir);
	dir->setIndex(fileName);
}

