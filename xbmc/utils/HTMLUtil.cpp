// according to http://www.w3.org/TR/2013/CR-html5-20130806/single-page.html#consume-a-character-reference
bool CHTMLUtil::TokenizeCharRef(const std::string& htmlText, size_t& pos, std::string& result)
{
  result.clear();
  const size_t len = htmlText.length();
  if (pos >= len)
    return false;

  const char* const htmlTextC = htmlText.c_str();
  if (htmlTextC[pos] != '&')
    return false;

  pos++; // consume '&'

  if (pos >= len || htmlTextC[pos] == 0 || htmlTextC[pos] == '\t' || htmlTextC[pos] == '\n' 
      || htmlTextC[pos] == 0x0C || htmlTextC[pos] == ' ' || htmlTextC[pos] == '<' || htmlTextC[pos] == '&')
      return true; // no parse error

  if (htmlTextC[pos] == '#') // "&#": character code
  {
    pos++; // consume '#'
    char32_t chr = 0;
    if (htmlTextC[pos] == 'x' || htmlTextC[pos] == 'X') // "&#xDDDD": character hex code
    {
      pos++; // consume 'X'
      if (!StringUtils::isasciixdigit(htmlTextC[pos]))
      { // no hex-digit after '&#x'
        pos -= 2; // unconsume 'X', unconsume '#'
        return false;
      }

      do 
      {
        chr *= 16;
        chr += StringUtils::asciixdigitvalue(htmlTextC[pos]);

        if (chr > 0x10FFFF)
        {
          chr = 0; // replace with same replacement char as char 0x00
          while (StringUtils::isasciixdigit(htmlTextC[pos])) { pos++; } // skip all hex-digits
        }
        else
          pos++;
      } while (StringUtils::isasciixdigit(htmlTextC[pos]));
    }
    else if (StringUtils::isasciidigit(htmlTextC[pos])) // "&#DDDD": character decimal code
    {
      do
      {
        chr *= 10;
        chr += StringUtils::asciidigitvalue(htmlTextC[pos]);

        if (chr > 0x10FFFF)
        {
          chr = 0; // replace with same replacement char as char 0x00
          while (StringUtils::isasciidigit(htmlTextC[pos])) { pos++; } // skip all digits
        }
        else
          pos++;
      } while (StringUtils::isasciidigit(htmlTextC[pos]));
    }
    else
    { // no digit or 'x' after '&#'
      pos--; // unconsume '#'
      return false;
    }

    bool errFlag = false;
    if (htmlTextC[pos] == ';')
      pos++; // ends with ';', consume ';'
    else
      errFlag = true;

    // check if character should be replaced
    replMapType::const_iterator const it = replacementMap.find(chr);
    if (it != replacementMap.end())
    {
      errFlag = true;
      chr = it->second;
    }
    else if (chr >= 0xD800 && chr <= 0xDFFF)
    {
      chr = 0xFFFD;
      return false;
    }
    else if ((chr >= 0x01 && chr <= 0x08) || (chr >= 0x0E && chr <= 0x1F)
             || (chr >= 0x7F && chr <= 0x9F) || (chr >= 0xFDD0 && chr <= 0xFDEF)
             || chr == 0x0B || (chr & 0xFFFF) == 0xFFFE || (chr & 0xFFFF) == 0xFFFF)
      errFlag = true; // indicate error, but do not replace character

    result = g_charsetConverter.charUtf32ToStrUtf8(chr);
    return !errFlag && !result.empty(); // check successful conversion to UTF-8
  } // htmlTextC[pos] == '#'
  
  
  pos--; // step back to '&' for matching named references
  // try to match named character reference
  for (int i = 0; HtmlNamedReferences[i].htmlStr; i++)
  {
    const HtmlNamedCharRef& namedRef = HtmlNamedReferences[i];
    if (htmlText.compare(pos, namedRef.htmlStrLen, namedRef.htmlStr, namedRef.htmlStrLen) == 0)
    {
      result = namedRef.utf8str;
      pos += namedRef.htmlStrLen;
      break;
    }
  }

  if (!result.empty())
    return true;

  // try to match "bad" named character reference
  for (int i = 0; HtmlNamedBadReferences[i].htmlStr; i++)
  {
    const HtmlNamedCharRef& namedRef = HtmlNamedBadReferences[i];
    if (htmlText.compare(pos, namedRef.htmlStrLen, namedRef.htmlStr, namedRef.htmlStrLen) == 0)
    {
      result = namedRef.utf8str;
      pos += namedRef.htmlStrLen;
      break;
    }
  }

  if (!result.empty())
    return false; // parse error: matched "bad" named reference (without terminating ';')

  // no named reference
  pos++; // consume again '&' (was unconsumed before named references match)
  size_t tmppos = pos;
  while (StringUtils::isasciialphanum(htmlTextC[tmppos])) { tmppos++; }
  if (tmppos != pos && htmlTextC[tmppos] == ';')
    return false; // parse error: '&' followed by one or more alpanums and ';'

  return true; // no match and no parse error
}


