void CHTMLUtil::ConvertHTMLToW(const CStdStringW& strHTML, CStdStringW& strStripped)
{
  /* TODO:STRING_CLEANUP */
  if (strHTML.size() == 0)
  {
    strStripped.clear();
    return ;
  }
  size_t iPos = 0;
  strStripped = strHTML;
  while (mappings[iPos].html)
  {
    StringUtils::Replace(strStripped, mappings[iPos].html,CStdStringW(1, mappings[iPos].w));
    iPos++;
  }

  iPos = strStripped.find(L"&#");
  while (iPos > 0 && iPos < strStripped.size() - 4)
  {
    size_t iStart = iPos + 1;
    iPos += 2;
    CStdStringW num;
    int base = 10;
    if (strStripped[iPos+1] == L'x')
    {
      base = 16;
      iPos++;
    }

    size_t i = iPos;
    while (iPos < strStripped.size() &&
           (base==16?iswxdigit(strStripped[iPos]):iswdigit(strStripped[iPos])))
      iPos++; 

    num = strStripped.substr(i, iPos-i);
    wchar_t val = (wchar_t)wcstol(num.c_str(),NULL,base);
    if (base == 10)
      num = StringUtils::Format(L"&#%ls;", num.c_str());
    else
      num = StringUtils::Format(L"&#x%ls;", num.c_str());

    StringUtils::Replace(strStripped, num,CStdStringW(1,val));
    iPos = strStripped.find(L"&#", iStart);
  }
}

bool CHTMLUtil::DecodeHTMLCharRefs(const std::string& htmlText, std::string& decodedText)
{
  decodedText.clear();
  const size_t len = htmlText.length();
  const char* const htmlTextC = htmlText.c_str();
  size_t pos = 0;
  bool parseError = false;

  // according to http://www.w3.org/TR/2013/CR-html5-20130806/single-page.html#preprocessing-the-input-stream
  if (len >= 3 && htmlTextC[0] == (char)0xEF && htmlTextC[1] == (char)0xBB && htmlTextC[2] == (char)0xBF)
    pos += 3; // strip UTF-8 BOM

  decodedText.reserve(len);
  while (pos < len)
  {
    // according to http://www.w3.org/TR/2013/CR-html5-20130806/single-page.html#preprocessing-the-input-stream
    const char currChar = htmlTextC[pos];
    if (currChar == '\r')
    {
      decodedText.push_back('\n'); // convert CR to LF
      pos++;
      if (htmlTextC[pos] == '\n')
        pos++; // ignore LF after CR
      continue;
    }
    if (currChar == 0)
    {
      pos++; // skip NULL character inside text
      parseError = true;
      continue;
    }
    if (currChar == '&')
    {
      // according to http://www.w3.org/TR/2013/CR-html5-20130806/single-page.html#data-state
      std::string token;
      if (!TokenizeCharRef(htmlText, pos, token))
        parseError = true;
      if (token.empty())
        decodedText.push_back('&');
      else
        decodedText.append(token);
      // '&' is always consumed by TokenizeCharRef
      continue;
    }
    if (!parseError)
    {
      const char nextChar1 = htmlTextC[pos + 1];
      const char nextChar2 = (pos - len >= 2) ? htmlTextC[pos + 2] : 0;
      const char nextChar3 = (pos - len >= 3) ? htmlTextC[pos + 3] : 0;

      if ((currChar >= 0x01 && currChar <= 0x08) || currChar == 0x0B || (currChar >= 0x0E && currChar <= 0x1F) || currChar == (char)0x7F
          || (currChar == (char)0xC2 && nextChar1 >= (char)0x80 && nextChar1 <= (char)0x9F) // U+80 - U+9F in UTF-8
          || (currChar == (char)0xEF && len - pos >= 3 && ((nextChar1 == (char)0xB7 && nextChar2 >= (char)0x90 && nextChar2 <= (char)0xAF) // U+FDD0 - U+FDEF in UTF-8
                                                           || (nextChar1 == (char)0xBF && nextChar2 >= (char)0xBE && nextChar2 <= (char)0xBF))) // U+FFFE - U+FFFF in UTF-8
          || (len - pos >= 4 && nextChar2 == (char)0xBF && nextChar3 >= (char)0xBE && nextChar3 <= (char)0xBF
              && ( (currChar == (char)0xF0 && (nextChar1 == (char)0x9F || nextChar1 == (char)0xAF || nextChar1 == (char)0xBF)) // U+(1-3)FFFE - U+(1-3)FFFF
                   || (currChar >= (char)0xF1 && currChar <= (char)0xF3 && (nextChar1 == (char)0x8F || nextChar1 == (char)0x9F || nextChar1 == (char)0xAF || nextChar1 == (char)0xBF)) // U+(4-F)FFFE - U+(4-F)FFFF
                   || (currChar == (char)0xF4 && nextChar1 == (char)0x8F) ) )// U+10FFFE - U+10FFFF
                   )
          parseError = true;
    }
    decodedText.push_back(currChar);
    pos++;
  }
  return !parseError;
}

